import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        // 백준_2741_N찍기

        Scanner sc = new Scanner(System.in);

        int num_o = sc.nextInt();

        for (int i = 1; i <= num_o; i++) {
            System.out.println(i);
        }

    }
}